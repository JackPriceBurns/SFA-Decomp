GHIDRA_FILE_RE = re.compile(r"(?P<addr>[0-9a-fA-F]{8})_(?P<name>[A-Za-z0-9_]+)\.c$")
CALL_RE = re.compile(r"\b([A-Za-z_]\w*)\s*\(")
GLOBAL_RE = re.compile(r"\b(_?DAT_[0-9a-fA-F]+|PTR_[A-Za-z0-9_]+|FLOAT_[0-9a-fA-F]+|DOUBLE_[0-9a-fA-F]+)\b")
UNSUPPORTED_TOKEN_RE = re.compile(r"\b(?:undefined[3567]|u?int3|u?int5|u?int6|u?int7|float10)\b")
INVALID_SLICE_RE = re.compile(r"\._\d+_\d+_")
HELPER_TOKEN_RE = re.compile(r"\b(?:CONCAT\d+|SUB\d+|SEXT\d+|ZEXT\d+|CARRY\d+|SCARRY\d+|SBORROW\d+)\b")


@dataclass(frozen=True)
class SplitSpan:
    owner: str
    start: int
    end: int


@dataclass
class FunctionDump:
    address: int
    name: str
    raw_text: str
    signature_text: str
    return_type: str
    safe_signature: bool
    safe_body: bool
    unsafe_reasons: list[str]
    called_functions: list[str]
    globals_used: list[str]

    @property
    def is_safe(self) -> bool:
        return self.safe_signature and self.safe_body


def normalize(path: str) -> str:
    return path.replace("\\", "/")


def write_text_if_changed(path: Path, text: str) -> bool:
    if path.is_file() and path.read_text(encoding="utf-8") == text:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8", newline="\n")
    return True


def strip_comments(text: str) -> str:
    text = re.sub(r"/\*.*?\*/", "", text, flags=re.S)
    text = re.sub(r"//.*", "", text)
    return text


def parse_splits(splits_path: Path) -> list[SplitSpan]:
    spans: list[SplitSpan] = []
    current_owner: str | None = None
    line_re = re.compile(r"start:0x([0-9A-Fa-f]+) end:0x([0-9A-Fa-f]+)")
    for raw_line in splits_path.read_text(encoding="utf-8").splitlines():
        if raw_line and not raw_line.startswith("\t") and raw_line.endswith(":"):
            current_owner = raw_line[:-1]
            continue
        if current_owner is None:
            continue
        line = raw_line.strip()
        if not line.startswith(".text"):
            continue
        match = line_re.search(line)
        if match is None:
            continue
        spans.append(SplitSpan(current_owner, int(match.group(1), 16), int(match.group(2), 16)))
    return spans


def owner_for_address(spans: Iterable[SplitSpan], address: int) -> SplitSpan | None:
    for span in spans:
        if span.start <= address < span.end:
            return span
    return None


def should_manage_existing(path: Path, overwrite_existing: bool) -> bool:
    if not path.exists():
        return True
    if overwrite_existing:
        return True
    head = path.read_text(encoding="utf-8", errors="replace")[:1024]
    return (
        GENERATED_MARKER in head
        or SOURCE_MATERIALIZE_MARKER in head
        or (NON_BUILT_STUB_MARKER in head and "#include" not in head)
    )


def parse_signature_block(raw_text: str, name: str) -> tuple[str, str]:
    lines = raw_text.splitlines()
    target_line = None
    for index, line in enumerate(lines):
        if re.search(rf"\b{name}\s*\(", line):
            target_line = index
            break
    if target_line is None:
        raise ValueError(f"Could not find signature for {name}")

    start = target_line
    while start > 0:
        previous = lines[start - 1].strip()
        if not previous or previous.startswith("//") or previous.startswith("/*"):
            break
        start -= 1

    collected: list[str] = []
    depth = 0
    seen_open = False
    for line in lines[start:]:
        stripped = line.rstrip()
        if stripped.strip() == "{":
            break
        collected.append(stripped)
        depth += stripped.count("(") - stripped.count(")")
        if "(" in stripped:
            seen_open = True
        if seen_open and depth <= 0 and ")" in stripped:
            break

    signature_text = "\n".join(collected).strip()
    name_index = signature_text.find(name)
    return_type = signature_text[:name_index].strip() if name_index != -1 else "undefined4"
    if not return_type:
        return_type = "undefined4"
    return signature_text, return_type


def simplify_return_type(return_type: str) -> str:
    lowered = return_type.replace("\n", " ").strip()
    if "*" in lowered:
        return "void*"
    if lowered == "void":
        return "void"
    if "double" in lowered or lowered == "f64":
        return "double"
    if "float" in lowered or lowered == "f32":
        return "float"
    if any(token in lowered for token in ("int", "short", "char", "long", "s8", "s16", "s32", "s64", "u8", "u16", "u32", "u64", "uint", "ulong", "undefined", "byte", "bool")):
        return "undefined4"
    return "undefined4"


def make_safe_forward_declaration(function: FunctionDump) -> str:
    if function.safe_signature:
        return function.signature_text + ";"
    return f"{simplify_return_type(function.return_type)} {function.name}();"


def default_return_statement(return_type: str) -> str | None:
    simplified = simplify_return_type(return_type)
    if simplified == "void":
        return None
    if simplified == "double":
        return "    return 0.0;"
    if simplified == "float":
        return "    return 0.0f;"
    return "    return 0;"


def detect_calls(raw_text: str, current_name: str) -> list[str]:
    raw_text = strip_comments(raw_text)
    calls: set[str] = set()
    for match in CALL_RE.finditer(raw_text):
        token = match.group(1)
        if token == current_name:
            continue
        if token in CONTROL_TOKENS or token in SUPPORTED_HELPERS or token in KNOWN_TYPES:
            continue
        if token.startswith("LAB_"):
            continue
        calls.add(token)
    return sorted(calls)


def detect_globals(raw_text: str) -> list[str]:
    raw_text = strip_comments(raw_text)
    return sorted(set(GLOBAL_RE.findall(raw_text)))


def check_safety(raw_text: str, signature_text: str) -> tuple[bool, bool, list[str]]:
    raw_text = strip_comments(raw_text)
    reasons: list[str] = []
    safe_signature = True
    safe_body = True

    if UNSUPPORTED_TOKEN_RE.search(signature_text):
        reasons.append("unsupported signature pseudo-types")
        safe_signature = False

    if INVALID_SLICE_RE.search(raw_text):
        reasons.append("invalid Ghidra slice syntax")
        safe_body = False

    unsupported_helpers = {
        helper
        for helper in HELPER_TOKEN_RE.findall(raw_text)
        if helper not in SUPPORTED_HELPERS
    }
    if unsupported_helpers:
        reasons.append(f"unsupported helper macros: {', '.join(sorted(unsupported_helpers))}")
        safe_body = False

    return safe_signature, safe_body, reasons


def load_function_dump(path: Path) -> FunctionDump:
    match = GHIDRA_FILE_RE.match(path.name)
    if match is None:
        raise ValueError(f"Unexpected Ghidra dump filename: {path.name}")
    raw_text = path.read_text(encoding="utf-8")
    name = match.group("name")
    signature_text, return_type = parse_signature_block(raw_text, name)
    safe_signature, safe_body, unsafe_reasons = check_safety(raw_text, signature_text)
    return FunctionDump(
        address=int(match.group("addr"), 16),
        name=name,
        raw_text=raw_text.rstrip() + "\n",
        signature_text=signature_text,
        return_type=return_type,
        safe_signature=safe_signature,
        safe_body=safe_body,
        unsafe_reasons=unsafe_reasons,
        called_functions=detect_calls(raw_text, name),
        globals_used=detect_globals(raw_text),
    )


def extern_decl_for_global(symbol: str) -> str:
    if symbol.startswith("DOUBLE_"):
        return f"extern f64 {symbol};"
    if symbol.startswith("FLOAT_"):
        return f"extern f32 {symbol};"
    if symbol.startswith("PTR_"):
        return f"extern void* {symbol};"
    return f"extern undefined4 {symbol};"


def header_relpath_for_owner(owner: str) -> str:
    return normalize(owner).removesuffix(".c") + ".h"


def header_guard(relpath: str) -> str:
    return re.sub(r"[^A-Za-z0-9]", "_", relpath.upper()) + "_"


def render_stub(function: FunctionDump) -> str:
    lines: list[str] = []
    reason = ", ".join(function.unsafe_reasons)
    lines.append(f"/* Auto-stubbed for compileability: {reason}. */")
    lines.append("#if 0")
    lines.append(function.raw_text.rstrip())
    lines.append("#endif")
    lines.append("")
    if function.safe_signature:
        lines.append(function.signature_text)
    else:
        lines.append(f"{simplify_return_type(function.return_type)}")
        lines.append(f"{function.name}()")
    lines.append("{")
    lines.append("    /* TODO: replace this stub with a cleaned-up Ghidra body. */")
    return_stmt = default_return_statement(function.return_type)
    if return_stmt is not None:
        lines.append(return_stmt)
    lines.append("}")
    return "\n".join(lines) + "\n"


def render_source(
    owner: str,
    span: SplitSpan,
    functions: list[FunctionDump],
    emit_headers: bool,
) -> str:
    local_names = {function.name for function in functions}
    extern_functions = sorted({call for function in functions for call in function.called_functions if call not in local_names})
    extern_globals = sorted({symbol for function in functions for symbol in function.globals_used})

    lines: list[str] = [
        "/*",
        f" * {GENERATED_MARKER}",
        " *",
        f" * Owner: {owner}",
        f" * Text span: 0x{span.start:08X}-0x{span.end:08X}",
        f" * Imported Ghidra functions: {len(functions)}",
        f" * Verbatim-safe functions: {sum(1 for function in functions if function.is_safe)}",
        f" * Auto-stubbed functions: {sum(1 for function in functions if not function.is_safe)}",
        " */",
        "",
        '#include "ghidra_import.h"',
    ]
    if emit_headers:
        lines.append(f'#include "{header_relpath_for_owner(owner)}"')
    lines.append("")

    if extern_functions:
        lines.append("/* Cross-file calls lifted from the raw Ghidra output. */")
        for name in extern_functions:
            lines.append(f"extern undefined4 {name}();")
        lines.append("")

    if extern_globals:
        lines.append("/* Raw global references kept as loose externs for later cleanup. */")
        for symbol in extern_globals:
            lines.append(extern_decl_for_global(symbol))
        lines.append("")

    lines.append("/* Local declarations keep old-style call compatibility across imported functions. */")
    for function in functions:
        lines.append(make_safe_forward_declaration(function))
    lines.append("")

    for function in functions:
        if function.is_safe:
            lines.append(function.raw_text.rstrip())
        else:
            lines.append(render_stub(function).rstrip())
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def render_header(owner: str, functions: list[FunctionDump]) -> str:
    relpath = header_relpath_for_owner(owner)
    guard = header_guard(relpath)
    lines = [
        f"#ifndef {guard}",
        f"#define {guard}",
        "",
        '#include "ghidra_import.h"',
        "",
    ]
    for function in functions:
        lines.append(make_safe_forward_declaration(function))
    lines.extend(["", f"#endif /* {guard} */", ""])
    return "\n".join(lines)


def update_configure_main(configure_path: Path, owners: list[str]) -> int:
    text = configure_path.read_text(encoding="utf-8")
    lib_anchor = '        "lib": "main",'
    objects_anchor = '        "objects": ['
    lib_start = text.find(lib_anchor)
    if lib_start == -1:
        raise SystemExit("Could not find the main library block in configure.py")
    objects_start = text.find(objects_anchor, lib_start)
    if objects_start == -1:
        raise SystemExit("Could not find the main object list in configure.py")
    list_end = text.find("        ],", objects_start)
    if list_end == -1:
        raise SystemExit("Could not find the end of the main object list in configure.py")

    existing = set(
        re.findall(r'Object\([^,]+,\s*"([^"]+)"', text[objects_start:list_end])
    )
    inserts = [
        f'            Object(NonMatching, "{owner}"),'
        for owner in owners
        if owner.startswith("main/") and owner not in existing
    ]
    if not inserts:
        return 0

    updated = text[:list_end] + "\n".join(inserts) + "\n" + text[list_end:]
    if updated != text:
        configure_path.write_text(updated, encoding="utf-8", newline="\n")
    return len(inserts)


def collect_selected_owners(args: argparse.Namespace, spans: list[SplitSpan]) -> list[str]:
    selected: list[str] = []

    if args.owner:
        selected.extend(normalize(owner) for owner in args.owner)

    if args.address:
        for raw_address in args.address:
            address = int(raw_address, 0)
            span = owner_for_address(spans, address)
            if span is None:
                raise SystemExit(f"No split owner found for address 0x{address:08X}")
            selected.append(span.owner)

    if not selected:
        selected = [span.owner for span in spans]

    deduped: list[str] = []
    seen: set[str] = set()
    for owner in selected:
        if owner not in seen:
            deduped.append(owner)
            seen.add(owner)
    if args.limit is not None:
        deduped = deduped[: args.limit]
    return deduped


def build_owner_function_map(ghidra_dir: Path, spans: list[SplitSpan]) -> tuple[dict[str, list[FunctionDump]], dict[str, SplitSpan]]:
    owner_map: dict[str, list[FunctionDump]] = {}
    span_map = {span.owner: span for span in spans}
    for path in sorted(ghidra_dir.glob("*_*.c")):
        match = GHIDRA_FILE_RE.match(path.name)
        if match is None:
            continue
        address = int(match.group("addr"), 16)
        span = owner_for_address(spans, address)
        if span is None:
            continue
        owner_map.setdefault(span.owner, []).append(load_function_dump(path))
    for functions in owner_map.values():
        functions.sort(key=lambda function: function.address)
    return owner_map, span_map


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Bulk-route raw Ghidra function dumps into split-owned NonMatching C scaffolds."
    )
    parser.add_argument("--ghidra-dir", type=Path, default=DEFAULT_GHIDRA_DIR)
    parser.add_argument("--splits", type=Path, default=DEFAULT_SPLITS_PATH)
    parser.add_argument("--src-root", type=Path, default=DEFAULT_SRC_ROOT)
    parser.add_argument("--include-root", type=Path, default=DEFAULT_INCLUDE_ROOT)
    parser.add_argument("--configure", type=Path, default=DEFAULT_CONFIGURE_PATH)
    parser.add_argument("--owner", action="append", help="Specific split owner to materialize, e.g. main/objanim.c")
    parser.add_argument("--address", action="append", help="Address to resolve to a split owner, e.g. 0x8002EC4C")
    parser.add_argument("--limit", type=int, help="Limit selected owners after filtering")
    parser.add_argument("--write", action="store_true", help="Write generated sources instead of only printing a summary")
    parser.add_argument("--emit-headers", action="store_true", help="Also emit per-file headers under include/")
    parser.add_argument("--overwrite-existing", action="store_true", help="Allow overwriting non-managed source files")
    parser.add_argument("--emit-config-snippet", action="store_true", help="Print Object(NonMatching, ...) lines for the selected owners")
    parser.add_argument("--update-configure-main", action="store_true", help="Append selected main/* owners to the main object list in configure.py")
    args = parser.parse_args()

    spans = parse_splits(args.splits)
    owner_map, span_map = build_owner_function_map(args.ghidra_dir, spans)
    selected_owners = collect_selected_owners(args, spans)

    summaries: list[str] = []
    wrote_sources = 0
    wrote_headers = 0
    skipped_existing = 0

    for owner in selected_owners:
        functions = owner_map.get(owner)
        if not functions:
            continue
        span = span_map[owner]
        safe_count = sum(1 for function in functions if function.is_safe)
        stubbed_count = len(functions) - safe_count
        summaries.append(
            f"{owner}: funcs={len(functions)} safe={safe_count} stubbed={stubbed_count} "
            f"text=0x{span.start:08X}-0x{span.end:08X}"
        )

        if not args.write:
            continue

        source_path = args.src_root / normalize(owner)
        if not should_manage_existing(source_path, args.overwrite_existing):
            skipped_existing += 1
            continue

        source_text = render_source(owner, span, functions, args.emit_headers)
        if write_text_if_changed(source_path, source_text):
            wrote_sources += 1

        if args.emit_headers:
            header_path = args.include_root / header_relpath_for_owner(owner)
            if write_text_if_changed(header_path, render_header(owner, functions)):
                wrote_headers += 1

    if summaries:
        print("\n".join(summaries))
    else:
        print("No Ghidra-owned split files matched the current selection.")

    if args.write:
        print(
            f"\nWrote {wrote_sources} source file(s), {wrote_headers} header(s), "
            f"skipped {skipped_existing} existing non-managed file(s)."
        )

    if args.update_configure_main:
        added = update_configure_main(args.configure, selected_owners)
        print(f"Updated configure.py main object list with {added} new entr{'y' if added == 1 else 'ies'}.")

    if args.emit_config_snippet and selected_owners:
        print("\nSuggested configure.py lines:")
        for owner in selected_owners:
            if owner in owner_map:
                print(f'Object(NonMatching, "{owner}"),')


if __name__ == "__main__":
    main()
