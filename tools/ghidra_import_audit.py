#!/usr/bin/env python3
"""Audit raw Ghidra dump coverage against split ownership and materialized sources."""

from __future__ import annotations

import argparse
import re
from collections import Counter, defaultdict
from dataclasses import dataclass
from pathlib import Path

from ghidra_bulk_import import (
    CLAIM_AUTO_MARKER,
    DEFAULT_GHIDRA_DIR,
    DEFAULT_MANAGED_OWNERS_PATH,
    DEFAULT_SPLITS_PATH,
    DEFAULT_SRC_ROOT,
    GENERATED_MARKER,
    NON_BUILT_STUB_MARKER,
    SOURCE_MATERIALIZE_MARKER,
    load_managed_owners,
    FunctionDump,
    SplitSpan,
    load_function_dump,
    normalize,
    owner_for_address,
    parse_splits,
    source_path_to_owner,
    strip_comments,
)


NAME_RE = re.compile(r"\b([A-Za-z_]\w*)\s*\(")
HOME_GENERATED_MARKER = "Auto-generated by tools/ghidra_unmanaged_homes.py."
INIT_HOME_GENERATED_MARKER = "Auto-generated by tools/ghidra_init_homes.py."
DEFAULT_HOME_ROOT = Path("src/ghidra_homes")
IGNORED_OWNER_PREFIXES = (
    "dolphin/",
    "Runtime.PPCEABI.H/",
)


@dataclass(frozen=True)
class GapWindow:
    start: int
    end: int
    previous_owner: str | None
    next_owner: str | None
    functions: tuple[FunctionDump, ...]

    @property
    def suggested_owner(self) -> str:
        anchor = self.functions[0].address if self.functions else self.start
        return f"main/unknown/autos/placeholder_{anchor:08X}.c"

    @property
    def safe_count(self) -> int:
        return sum(1 for function in self.functions if function.is_safe)

    @property
    def unsafe_count(self) -> int:
        return len(self.functions) - self.safe_count


def classify_source_state(path: Path) -> str:
    if not path.exists():
        return "missing_file"
    owner = source_path_to_owner(path)
    if owner is not None and owner in load_managed_owners(DEFAULT_MANAGED_OWNERS_PATH):
        return "ghidra_generated"
    head = path.read_text(encoding="utf-8", errors="ignore")[:1200]
    if GENERATED_MARKER in head:
        return "ghidra_generated"
    if CLAIM_AUTO_MARKER in head:
        return "claim_auto"
    if SOURCE_MATERIALIZE_MARKER in head:
        return "source_materialize"
    if NON_BUILT_STUB_MARKER in head:
        return "non_built_stub"
    return "unmanaged"


def source_present_names(path: Path) -> set[str]:
    if not path.exists():
        return set()
    text = strip_comments(path.read_text(encoding="utf-8", errors="ignore"))
    return {match.group(1) for match in NAME_RE.finditer(text)}


def owner_root(owner: str) -> str:
    if owner.startswith("dolphin/"):
        return "dolphin"
    if owner.startswith("Runtime.PPCEABI.H/"):
        return "Runtime.PPCEABI.H"
    if owner.startswith("main/audio/"):
        return "main/audio"
    if owner.startswith("main/"):
        return "main"
    if owner.startswith("track/"):
        return "track"
    return "other"


def is_ignored_non_game_owner(owner: str) -> bool:
    return normalize(owner).startswith(IGNORED_OWNER_PREFIXES)


def parse_section_spans(splits_path: Path, sections: set[str]) -> list[SplitSpan]:
    spans: list[SplitSpan] = []
    current_owner: str | None = None
    owner_re = re.compile(r"^([^\t][^:]*):")
    line_re = re.compile(r"start:0x([0-9A-Fa-f]+) end:0x([0-9A-Fa-f]+)")
    for raw_line in splits_path.read_text(encoding="utf-8").splitlines():
        owner_match = owner_re.match(raw_line)
        if owner_match:
            current_owner = owner_match.group(1)
            continue
        if current_owner is None:
            continue
        line = raw_line.strip()
        section = line.split()[0] if line else ""
        if section not in sections:
            continue
        match = line_re.search(line)
        if match is None:
            continue
        spans.append(
            SplitSpan(
                current_owner,
                int(match.group(1), 16),
                int(match.group(2), 16),
            )
        )
    return spans


def home_output_path(home_root: Path, owner: str) -> Path:
    relpath = Path(normalize(owner))
    if relpath.suffix != ".c":
        relpath = relpath.with_suffix(".c")
    return home_root / relpath


def has_companion_home(path: Path) -> bool:
    if not path.exists():
        return False
    head = path.read_text(encoding="utf-8", errors="ignore")[:1200]
    return (
        HOME_GENERATED_MARKER in head
        or INIT_HOME_GENERATED_MARKER in head
    )


def collect_gap_windows(
    spans: list,
    unowned_functions: list[FunctionDump],
) -> list[GapWindow]:
    ordered_spans = sorted(spans, key=lambda span: span.start)
    if not unowned_functions:
        return []

    gaps: list[tuple[int, int, str | None, str | None]] = []
    if ordered_spans and unowned_functions[0].address < ordered_spans[0].start:
        gaps.append((0, ordered_spans[0].start, None, ordered_spans[0].owner))

    for index in range(len(ordered_spans) - 1):
        current = ordered_spans[index]
        nxt = ordered_spans[index + 1]
        if current.end < nxt.start:
            gaps.append((current.end, nxt.start, current.owner, nxt.owner))

    if ordered_spans and unowned_functions[-1].address >= ordered_spans[-1].end:
        gaps.append(
            (
                ordered_spans[-1].end,
                unowned_functions[-1].address + max(unowned_functions[-1].size, 1),
                ordered_spans[-1].owner,
                None,
            )
        )

    windows: list[GapWindow] = []
    for start, end, previous_owner, next_owner in gaps:
        functions = tuple(
            function
            for function in unowned_functions
            if start <= function.address < end
        )
        if not functions:
            continue
        windows.append(
            GapWindow(
                start=start,
                end=end,
                previous_owner=previous_owner,
                next_owner=next_owner,
                functions=functions,
            )
        )
    return windows


def print_summary(
    raw_count: int,
    state_counts: Counter[str],
    protected_root_counts: Counter[str],
    protected_owner_sets: dict[str, set[str]],
    protected_home_owner_sets: dict[str, set[str]],
    protected_home_count: int,
    protected_missing_homes: list[str],
    ignored_unmanaged_count: int,
    init_home_owner_count: int,
    init_home_count: int,
    init_missing_homes: list[str],
    ignored_init_count: int,
    managed_mismatches: list[tuple[str, str, int]],
    gap_windows: list[GapWindow],
    unowned_reason_counts: Counter[str],
) -> None:
    protected_count = state_counts["unmanaged"] - ignored_unmanaged_count
    unowned_count = state_counts["unowned"]
    init_missing_count = state_counts["init_no_c_home"]
    managed_count = (
        raw_count
        - protected_count
        - ignored_unmanaged_count
        - unowned_count
        - init_home_count
        - init_missing_count
        - ignored_init_count
    )
    no_c_home_count = (
        (protected_count - protected_home_count)
        + unowned_count
    )

    print("# Ghidra import audit")
    print(f"- raw dumps: `{raw_count}`")
    print(f"- managed/generated imports already materialized: `{managed_count}`")
    print(f"- protected by existing unmanaged source: `{protected_count}`")
    if ignored_unmanaged_count:
        print(f"- ignored non-game unmanaged dumps: `{ignored_unmanaged_count}`")
    if protected_count:
        print(f"- unmanaged dumps with companion `.c` homes: `{protected_home_count}`")
        print(f"- unmanaged owners still missing companion homes: `{len(protected_missing_homes)}`")
    if init_home_count or init_missing_count or ignored_init_count:
        print(f"- `.init` runtime dumps with companion `.c` homes: `{init_home_count}`")
        print(f"- `.init` runtime owners still missing companion homes: `{len(init_missing_homes)}`")
        if ignored_init_count:
            print(f"- ignored runtime `.init` dumps: `{ignored_init_count}`")
    print(f"- raw dumps still lacking any `.c` home: `{no_c_home_count}`")
    print(f"- no split owner / currently unimportable: `{unowned_count}`")
    print(f"- managed owner mismatches: `{len(managed_mismatches)}`")

    print("\n## Why The Tool Missed Them")
    print(
        "- `ghidra_bulk_import.py` only routes functions whose entry address lands inside an existing "
        "`splits.txt` text span."
    )
    print(
        "- Functions under unmanaged source files are intentionally skipped by `should_manage_existing()` "
        "to avoid overwriting existing work."
    )
    print(
        "- Functions in split gaps never reach render/write at all because `build_owner_function_map()` "
        "drops unowned addresses."
    )
    print(
        "- Runtime/SDK owners under `dolphin/*` and `Runtime.PPCEABI.H/*` are intentionally out of scope "
        "for the game-code companion-home path."
    )

    print("\n## Protected Existing Owners")
    if not protected_root_counts:
        print("- none")
    else:
        for root, count in protected_root_counts.most_common():
            owner_count = len(protected_owner_sets[root])
            home_owner_count = len(protected_home_owner_sets[root])
            print(
                f"- `{root}`: `{count}` raw dumps across `{owner_count}` existing owner files "
                f"(`{home_owner_count}` with companion homes)"
            )

    print("\n## Unmanaged Companion Homes")
    if not protected_count:
        print("- none")
    else:
        print(f"- companion-home-covered dumps: `{protected_home_count}` / `{protected_count}`")
        print(
            f"- companion-home-covered owners: "
            f"`{sum(len(owners) for owners in protected_home_owner_sets.values())}` / "
            f"`{sum(len(owners) for owners in protected_owner_sets.values())}`"
        )
        if protected_missing_homes:
            for owner in protected_missing_homes[:20]:
                print(f"- missing home: `{owner}`")
        else:
            print("- every unmanaged owner has a generated non-built `.c` home")

    print("\n## Init Runtime Homes")
    init_total = init_home_count + state_counts["init_no_c_home"]
    if not init_total:
        if ignored_init_count:
            print(f"- ignored runtime `.init` dumps: `{ignored_init_count}`")
        else:
            print("- none")
    else:
        print(f"- companion-home-covered dumps: `{init_home_count}` / `{init_total}`")
        print(
            f"- companion-home-covered owners: `{init_home_owner_count}` / "
            f"`{init_home_owner_count + len(init_missing_homes)}`"
        )
        if init_missing_homes:
            for owner in init_missing_homes[:20]:
                print(f"- missing home: `{owner}`")
        else:
            print("- every `.init` assembly owner with raw dumps has a generated non-built `.c` home")

    print("\n## Unowned Gap Summary")
    if not gap_windows:
        print("- none")
    else:
        safe_total = sum(window.safe_count for window in gap_windows)
        unsafe_total = sum(window.unsafe_count for window in gap_windows)
        print(f"- gap windows with raw dumps: `{len(gap_windows)}`")
        print(f"- automatable verbatim imports after adding owners: `{safe_total}`")
        print(f"- automatable compile-first stubs after adding owners: `{unsafe_total}`")
        if unowned_reason_counts:
            print("- top current stub blockers among unowned dumps:")
            for reason, count in unowned_reason_counts.most_common(8):
                print(f"  - `{count}`: {reason}")


def print_gap_windows(gap_windows: list[GapWindow], limit: int, sort_by: str) -> None:
    print("\n## Largest Unowned Gaps")
    if not gap_windows:
        print("- none")
        return

    if sort_by == "count":
        ordered = sorted(
            gap_windows,
            key=lambda window: (-len(window.functions), window.start),
        )
    else:
        ordered = sorted(gap_windows, key=lambda window: window.start)

    for window in ordered[:limit]:
        print(
            f"- `0x{window.start:08X}-0x{window.end:08X}` funcs=`{len(window.functions)}` "
            f"safe=`{window.safe_count}` stub=`{window.unsafe_count}` "
            f"suggested=`{window.suggested_owner}`"
        )
        print(
            f"  prev=`{window.previous_owner or '-'}` next=`{window.next_owner or '-'}`"
        )
        names = ", ".join(function.name for function in window.functions[:6])
        if len(window.functions) > 6:
            names += ", ..."
        print(f"  names=`{names}`")


def print_protected_owners(
    protected_by_owner: dict[str, list[FunctionDump]],
    protected_home_owners: set[str],
    limit: int,
) -> None:
    print("\n## Largest Protected Existing Owners")
    if not protected_by_owner:
        print("- none")
        return

    ordered = sorted(
        protected_by_owner.items(),
        key=lambda item: (-len(item[1]), item[0]),
    )
    for owner, functions in ordered[:limit]:
        home_state = "yes" if owner in protected_home_owners else "no"
        print(
            f"- `{owner}` raw_dumps=`{len(functions)}` category=`{owner_root(owner)}` "
            f"companion_home=`{home_state}` action=`manual/no-overwrite`"
        )


def print_managed_mismatches(managed_mismatches: list[tuple[str, str, int]]) -> None:
    print("\n## Managed Owner Mismatches")
    if not managed_mismatches:
        print("- none")
        return
    for owner, state, missing_count in managed_mismatches:
        print(f"- `{owner}` state=`{state}` missing_names=`{missing_count}`")


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Audit raw Ghidra dump coverage against split ownership and source materialization."
    )
    parser.add_argument("--ghidra-dir", type=Path, default=DEFAULT_GHIDRA_DIR)
    parser.add_argument("--splits", type=Path, default=DEFAULT_SPLITS_PATH)
    parser.add_argument("--src-root", type=Path, default=DEFAULT_SRC_ROOT)
    parser.add_argument("--home-root", type=Path, default=DEFAULT_HOME_ROOT)
    parser.add_argument(
        "--limit",
        type=int,
        default=15,
        help="Maximum owners/gaps to print in detail sections.",
    )
    parser.add_argument(
        "--sort-gaps",
        choices=("count", "address"),
        default="count",
        help="Sort detailed gap output by size or address.",
    )
    args = parser.parse_args()

    spans = parse_splits(args.splits)
    protected_by_owner: dict[str, list[FunctionDump]] = defaultdict(list)
    owner_state: dict[str, str] = {}
    owner_functions: dict[str, list[FunctionDump]] = defaultdict(list)
    protected_root_counts: Counter[str] = Counter()
    protected_owner_sets: dict[str, set[str]] = defaultdict(set)
    protected_home_owner_sets: dict[str, set[str]] = defaultdict(set)
    protected_home_state: dict[str, bool] = {}
    init_home_owners: set[str] = set()
    init_missing_homes: list[str] = []
    unowned_functions: list[FunctionDump] = []
    state_counts: Counter[str] = Counter()
    protected_missing_homes: list[str] = []
    protected_home_count = 0
    ignored_unmanaged_count = 0

    init_spans = parse_section_spans(args.splits, {".init"})
    raw_paths = sorted(args.ghidra_dir.glob("*_*.c"))
    for path in raw_paths:
        function = load_function_dump(path)
        span = owner_for_address(spans, function.address)
        if span is None:
            init_span = owner_for_address(init_spans, function.address)
            if init_span is not None and normalize(init_span.owner).endswith(".s"):
                state_counts["ignored_init"] += 1
                continue
            unowned_functions.append(function)
            state_counts["unowned"] += 1
            continue

        owner = span.owner
        source_path = args.src_root / normalize(owner)
        state = owner_state.setdefault(owner, classify_source_state(source_path))
        state_counts[state] += 1
        owner_functions[owner].append(function)
        if state == "unmanaged":
            if is_ignored_non_game_owner(owner):
                ignored_unmanaged_count += 1
                continue
            protected_by_owner[owner].append(function)
            root = owner_root(owner)
            protected_root_counts[root] += 1
            protected_owner_sets[root].add(owner)
            has_home = protected_home_state.setdefault(
                owner,
                has_companion_home(home_output_path(args.home_root, owner)),
            )
            if has_home:
                protected_home_count += 1
                protected_home_owner_sets[root].add(owner)

    managed_states = {"ghidra_generated", "claim_auto", "source_materialize", "non_built_stub"}
    managed_mismatches: list[tuple[str, str, int]] = []
    for owner, functions in sorted(owner_functions.items()):
        state = owner_state[owner]
        if state not in managed_states:
            continue
        present_names = source_present_names(args.src_root / normalize(owner))
        missing_count = sum(1 for function in functions if function.name not in present_names)
        if missing_count:
            managed_mismatches.append((owner, state, missing_count))

    gap_windows = collect_gap_windows(spans, sorted(unowned_functions, key=lambda function: function.address))
    unowned_reason_counts: Counter[str] = Counter()
    for function in unowned_functions:
        for reason in function.unsafe_reasons:
            unowned_reason_counts[reason] += 1

    protected_home_owners = {
        owner
        for owners in protected_home_owner_sets.values()
        for owner in owners
    }
    for owner in sorted(protected_by_owner):
        if owner not in protected_home_owner_sets[owner_root(owner)]:
            protected_missing_homes.append(owner)
    init_missing_homes = sorted(set(init_missing_homes))

    print_summary(
        raw_count=len(raw_paths),
        state_counts=state_counts,
        protected_root_counts=protected_root_counts,
        protected_owner_sets=protected_owner_sets,
        protected_home_owner_sets=protected_home_owner_sets,
        protected_home_count=protected_home_count,
        protected_missing_homes=protected_missing_homes,
        ignored_unmanaged_count=ignored_unmanaged_count,
        init_home_owner_count=len(init_home_owners),
        init_home_count=state_counts["init_companion_home"],
        init_missing_homes=init_missing_homes,
        ignored_init_count=state_counts["ignored_init"],
        managed_mismatches=managed_mismatches,
        gap_windows=gap_windows,
        unowned_reason_counts=unowned_reason_counts,
    )
    print_gap_windows(gap_windows, args.limit, args.sort_gaps)
    print_protected_owners(
        protected_by_owner,
        protected_home_owners,
        args.limit,
    )
    print_managed_mismatches(managed_mismatches)


if __name__ == "__main__":
    main()
