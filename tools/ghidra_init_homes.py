#!/usr/bin/env python3
"""Materialize non-built Ghidra companion homes for .init assembly owners."""

from __future__ import annotations

import argparse
import re
from pathlib import Path

from ghidra_bulk_import import (
    DEFAULT_GHIDRA_DIR,
    DEFAULT_SPLITS_PATH,
    FunctionDump,
    SplitSpan,
    function_stub_reasons,
    load_function_dump,
    normalize,
    owner_for_address,
    render_source,
    write_text_if_changed,
)


HOME_GENERATED_MARKER = "Auto-generated by tools/ghidra_init_homes.py."
DEFAULT_HOME_ROOT = Path("src/ghidra_homes")


def parse_section_spans(splits_path: Path, sections: set[str]) -> list[SplitSpan]:
    spans: list[SplitSpan] = []
    current_owner: str | None = None
    owner_re = re.compile(r"^([^\t][^:]*):")
    line_re = re.compile(r"start:0x([0-9A-Fa-f]+) end:0x([0-9A-Fa-f]+)")
    for raw_line in splits_path.read_text(encoding="utf-8").splitlines():
        owner_match = owner_re.match(raw_line)
        if owner_match:
            current_owner = owner_match.group(1)
            continue
        if current_owner is None:
            continue
        line = raw_line.strip()
        section = line.split()[0] if line else ""
        if section not in sections:
            continue
        match = line_re.search(line)
        if match is None:
            continue
        spans.append(
            SplitSpan(
                current_owner,
                int(match.group(1), 16),
                int(match.group(2), 16),
            )
        )
    return spans


def home_output_path(home_root: Path, owner: str) -> Path:
    relpath = Path(normalize(owner))
    if relpath.suffix != ".c":
        relpath = relpath.with_suffix(".c")
    return home_root / relpath


def render_companion_source(owner: str, span: SplitSpan, functions: list[FunctionDump]) -> str:
    stub_reasons = function_stub_reasons(owner, functions)
    safe_count = len(functions) - len(stub_reasons)
    stubbed_count = len(stub_reasons)
    home_owner = normalize(owner)
    if home_owner.endswith(".s"):
        home_owner = home_owner[:-2] + ".c"
    header = [
        "/*",
        f" * {HOME_GENERATED_MARKER}",
        " *",
        " * This file is intentionally not wired into the build yet.",
        " * It gives the .init runtime assembly span a concrete .c home for",
        " * decomp iteration while leaving the live assembly owner untouched.",
        " *",
        f" * Live owner: {owner}",
        f" * Companion owner: {home_owner}",
        f" * Section span: 0x{span.start:08X}-0x{span.end:08X}",
        f" * Imported Ghidra functions: {len(functions)}",
        f" * Verbatim-safe functions: {safe_count}",
        f" * Auto-stubbed functions: {stubbed_count}",
        " */",
        "",
    ]
    return "\n".join(header) + render_source(home_owner, span, functions, emit_headers=False)


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate non-built Ghidra companion homes for .init assembly owners."
    )
    parser.add_argument("--ghidra-dir", type=Path, default=DEFAULT_GHIDRA_DIR)
    parser.add_argument("--splits", type=Path, default=DEFAULT_SPLITS_PATH)
    parser.add_argument("--home-root", type=Path, default=DEFAULT_HOME_ROOT)
    parser.add_argument("--write", action="store_true", help="Write companion files to disk.")
    args = parser.parse_args()

    init_spans = [
        span
        for span in parse_section_spans(args.splits, {".init"})
        if normalize(span.owner).endswith(".s")
    ]
    owner_map: dict[str, list[FunctionDump]] = {span.owner: [] for span in init_spans}
    span_map = {span.owner: span for span in init_spans}

    for path in sorted(args.ghidra_dir.glob("*_*.c")):
        function = load_function_dump(path)
        span = owner_for_address(init_spans, function.address)
        if span is None:
            continue
        owner_map[span.owner].append(function)

    wrote = 0
    owner_count = 0
    total_functions = 0
    for owner in sorted(owner_map):
        functions = owner_map[owner]
        if not functions:
            continue
        owner_count += 1
        total_functions += len(functions)
        span = span_map[owner]
        out_path = home_output_path(args.home_root, owner)
        source_text = render_companion_source(owner, span, functions)
        safe_count = len(functions) - len(function_stub_reasons(owner, functions))
        stubbed_count = len(functions) - safe_count
        print(
            f"{owner}: funcs={len(functions)} safe={safe_count} stubbed={stubbed_count} "
            f"home={out_path.as_posix()}"
        )
        if args.write and write_text_if_changed(out_path, source_text):
            wrote += 1

    print(
        f"\n.init assembly owners: {owner_count} | "
        f"raw functions: {total_functions} | "
        f"{'wrote' if args.write else 'would write'}: {wrote if args.write else owner_count}"
    )


if __name__ == "__main__":
    main()
