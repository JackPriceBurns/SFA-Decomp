#!/usr/bin/env python3
"""Materialize non-built Ghidra companion homes for unmanaged owner files."""

from __future__ import annotations

import argparse
from pathlib import Path

from ghidra_bulk_import import (
    CLAIM_AUTO_MARKER,
    DEFAULT_GHIDRA_DIR,
    DEFAULT_MANAGED_OWNERS_PATH,
    DEFAULT_SPLITS_PATH,
    DEFAULT_SRC_ROOT,
    GENERATED_MARKER,
    NON_BUILT_STUB_MARKER,
    SOURCE_MATERIALIZE_MARKER,
    build_owner_function_map,
    function_stub_reasons,
    load_managed_owners,
    normalize,
    parse_splits,
    render_source,
    source_path_to_owner,
    write_text_if_changed,
)


HOME_GENERATED_MARKER = "Auto-generated by tools/ghidra_unmanaged_homes.py."
DEFAULT_HOME_ROOT = Path("src/ghidra_homes")
IGNORED_OWNER_PREFIXES = (
    "dolphin/",
    "Runtime.PPCEABI.H/",
)


def classify_source_state(path: Path) -> str:
    if not path.exists():
        return "missing_file"
    owner = source_path_to_owner(path)
    if owner is not None and owner in load_managed_owners(DEFAULT_MANAGED_OWNERS_PATH):
        return "ghidra_generated"
    head = path.read_text(encoding="utf-8", errors="ignore")[:1200]
    if GENERATED_MARKER in head:
        return "ghidra_generated"
    if CLAIM_AUTO_MARKER in head:
        return "claim_auto"
    if SOURCE_MATERIALIZE_MARKER in head:
        return "source_materialize"
    if NON_BUILT_STUB_MARKER in head:
        return "non_built_stub"
    return "unmanaged"


def render_companion_source(owner: str, span, functions: list) -> str:
    stub_reasons = function_stub_reasons(owner, functions)
    safe_count = len(functions) - len(stub_reasons)
    stubbed_count = len(stub_reasons)
    header = [
        "/*",
        f" * {HOME_GENERATED_MARKER}",
        " *",
        " * This file is intentionally not wired into the build yet.",
        " * It exists so every raw Ghidra function mapped to an unmanaged live owner",
        " * still has a concrete .c home for iteration/reference work.",
        " *",
        f" * Live owner: {owner}",
        f" * Text span: 0x{span.start:08X}-0x{span.end:08X}",
        f" * Imported Ghidra functions: {len(functions)}",
        f" * Verbatim-safe functions: {safe_count}",
        f" * Auto-stubbed functions: {stubbed_count}",
        " */",
        "",
    ]
    return "\n".join(header) + render_source(owner, span, functions, emit_headers=False)


def home_output_path(home_root: Path, owner: str) -> Path:
    relpath = Path(normalize(owner))
    if relpath.suffix != ".c":
        relpath = relpath.with_suffix(".c")
    return home_root / relpath


def should_materialize_companion_home(owner: str) -> bool:
    normalized = normalize(owner)
    return not normalized.startswith(IGNORED_OWNER_PREFIXES)


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate non-built Ghidra companion homes for unmanaged source owners."
    )
    parser.add_argument("--ghidra-dir", type=Path, default=DEFAULT_GHIDRA_DIR)
    parser.add_argument("--splits", type=Path, default=DEFAULT_SPLITS_PATH)
    parser.add_argument("--src-root", type=Path, default=DEFAULT_SRC_ROOT)
    parser.add_argument("--home-root", type=Path, default=DEFAULT_HOME_ROOT)
    parser.add_argument("--write", action="store_true", help="Write companion files to disk.")
    parser.add_argument(
        "--limit",
        type=int,
        help="Only process the first N unmanaged owners after sorting.",
    )
    args = parser.parse_args()

    spans = parse_splits(args.splits)
    owner_map, span_map = build_owner_function_map(args.ghidra_dir, spans)

    unmanaged_owners = [
        owner
        for owner in sorted(owner_map)
        if classify_source_state(args.src_root / normalize(owner)) == "unmanaged"
        and should_materialize_companion_home(owner)
    ]
    if args.limit is not None:
        unmanaged_owners = unmanaged_owners[: args.limit]

    wrote = 0
    total_functions = 0
    for owner in unmanaged_owners:
        functions = owner_map[owner]
        span = span_map[owner]
        total_functions += len(functions)
        out_path = home_output_path(args.home_root, owner)
        source_text = render_companion_source(owner, span, functions)
        safe_count = len(functions) - len(function_stub_reasons(owner, functions))
        stubbed_count = len(functions) - safe_count
        print(
            f"{owner}: funcs={len(functions)} safe={safe_count} stubbed={stubbed_count} "
            f"home={out_path.as_posix()}"
        )
        if args.write and write_text_if_changed(out_path, source_text):
            wrote += 1

    print(
        f"\nUnmanaged owners: {len(unmanaged_owners)} | "
        f"raw functions: {total_functions} | "
        f"{'wrote' if args.write else 'would write'}: {wrote if args.write else len(unmanaged_owners)}"
    )


if __name__ == "__main__":
    main()
