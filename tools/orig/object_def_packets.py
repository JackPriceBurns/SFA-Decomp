    widths = widths_by_def.get(record.def_id, Counter())
    width_values = tuple(sorted(widths))
    min_width_bytes = width_values[0] * 4 if width_values else None
    max_width_bytes = width_values[-1] * 4 if width_values else None
    reference_functions = function_name_by_index(packet.reference_dll)

    lines: list[str] = []
    lines.append("/*")
    lines.append(f" * {GENERATED_MARKER}")
    lines.append(" *")
    lines.append(" * This file is intentionally not wired into the build yet.")
