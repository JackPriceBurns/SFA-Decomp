@dataclass(frozen=True)
class DefPacket:
    record: ObjectRecord
    score: int
    boundary_kind: str
    class_name: str
    family_name: str
    output_name: str
    class_output_name: str
    family_output_name: str | None
    placement_widths: tuple[int, ...]
    same_class_records: tuple[ObjectRecord, ...]
    same_dll_records: tuple[ObjectRecord, ...]
    reference_object: ReferenceObjectHint | None
    reference_dll: ReferenceDllHint | None
    dll_entry: DllEntry | None


def slugify(value: str) -> str:
    slug = IDENTIFIER_RE.sub("_", value).strip("_").lower()
    return slug or "object"


def identifier_from_name(value: str) -> str:
    identifier = IDENTIFIER_RE.sub("_", value).strip("_")
    if not identifier:
        identifier = "unnamed"
    if identifier[0].isdigit():
        identifier = "_" + identifier
    return identifier


def write_text_if_changed(path: Path, text: str) -> bool:
    if path.is_file() and path.read_text(encoding="utf-8") == text:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8", newline="\n")
    return True


def is_managed_generated_stub(path: Path) -> bool:
    if not path.is_file():
        return False
    return GENERATED_MARKER in path.read_text(encoding="utf-8", errors="replace")[:512]


def resolve_reference_dll_path(explicit: Path | None) -> Path | None:
    return resolve_reference_path(explicit, DEFAULT_REFERENCE_DLL_CANDIDATES)


def format_widths(values: tuple[int, ...]) -> str:
    return ", ".join(f"{value}w" for value in values) if values else "none"


def format_width_counter(counter: Counter[int]) -> str:
    return ", ".join(f"{width}w x{count}" for width, count in sorted(counter.items())) or "none"


def format_hex_list(values: tuple[int, ...], width: int = 4) -> str:
    if not values:
        return "none"
    return " ".join(f"0x{value:0{width}X}" for value in values)


def format_record_path(packet: DefPacket) -> str:
    return f"src/main/unknown/objects/{packet.output_name}"


def class_packet_name(class_id: int, class_name: str, same_class_records: tuple[ObjectRecord, ...]) -> str:
    suggested_name = class_name or (same_class_records[0].name if len(same_class_records) == 1 else f"class_{class_id:04X}")
    return f"class_{class_id:04X}_{slugify(suggested_name)}.c"


def family_packet_name(
    dll_id: int,
    reference_dll: ReferenceDllHint | None,
    same_dll_records: tuple[ObjectRecord, ...],
) -> str | None:
    if dll_id == 0xFFFF:
        return None
    name_source = reference_dll.name if reference_dll is not None and reference_dll.name else None
    if name_source is None:
        name_source = same_dll_records[0].name if len(same_dll_records) == 1 else f"dll_{dll_id:04X}"
    return f"dll_{dll_id:04X}_{slugify(name_source)}.c"


def score_packet(
    record: ObjectRecord,
    widths: tuple[int, ...],
    same_class_records: tuple[ObjectRecord, ...],
    same_dll_records: tuple[ObjectRecord, ...],
    reference_object: ReferenceObjectHint | None,
    reference_dll: ReferenceDllHint | None,
    dll_entry: DllEntry | None,
) -> int:
    score = int((min(record.placements, 5000) ** 0.5) * 110)
    if len(widths) == 1:
        score += 90
    elif 1 < len(widths) <= 4:
        score += 40
    if len(same_class_records) == 1:
        score += 220
    elif len(same_class_records) <= 4:
        score += 120
    if len(same_dll_records) == 1:
        score += 180
    elif len(same_dll_records) <= 6:
        score += 80
    if dll_entry is not None and dll_entry.status == "descriptor":
        score += 110
        score += min(dll_entry.function_count or 0, 12) * 6
    if reference_object is not None:
        score += 70
        score += min(len(reference_object.params), 8) * 10
    if reference_dll is not None and reference_dll.fields:
        score += 40
    if record.map_name is not None:
        score += 20
    if record.inline_fields:
        score += min(len(record.inline_fields), 4) * 10
    return score


def classify_boundary(
    record: ObjectRecord,
    same_class_records: tuple[ObjectRecord, ...],
    same_dll_records: tuple[ObjectRecord, ...],
) -> str:
    class_dlls = {item.dll_id for item in same_class_records}
    if len(same_class_records) == 1 and len(same_dll_records) == 1:
        return "singleton object boundary"
    if len(same_class_records) == 1:
        return "single-def class inside shared DLL"
    if len(same_class_records) <= 4 and len(class_dlls) <= 2:
        return "tight class packet"
    if len(same_dll_records) <= 6:
        return "tight DLL family"
    if record.map_name is not None and len(same_class_records) <= 12:
        return "map-fixed class packet"
    return "shared DLL family"


def build_def_packets(
    records: list[ObjectRecord],
    widths_by_def: dict[int, Counter[int]],
    dll_entries: list[DllEntry],
    reference_class_names: dict[int, str],
    reference_objects_by_def: dict[int, ReferenceObjectHint],
    reference_objects_by_name: dict[str, ReferenceObjectHint],
    reference_dlls: dict[int, ReferenceDllHint],
) -> list[DefPacket]:
    records_by_class: dict[int, list[ObjectRecord]] = defaultdict(list)
    records_by_dll: dict[int, list[ObjectRecord]] = defaultdict(list)
    dll_entries_by_id = {entry.dll_id: entry for entry in dll_entries}

    for record in records:
        records_by_class[record.class_id & 0xFFFF].append(record)
        records_by_dll[record.dll_id].append(record)

    for items in records_by_class.values():
        items.sort(key=lambda item: (-item.placements, item.def_id))
    for items in records_by_dll.values():
        items.sort(key=lambda item: (-item.placements, item.def_id))

    packets: list[DefPacket] = []
    for record in records:
        same_class_records = tuple(records_by_class[record.class_id & 0xFFFF])
        same_dll_records = tuple(records_by_dll[record.dll_id])
        widths = tuple(sorted(widths_by_def.get(record.def_id, Counter())))
        class_name = reference_class_names.get(record.class_id & 0xFFFF, "")
        reference_object = reference_hint_for_record(record, reference_objects_by_def, reference_objects_by_name)
        reference_dll = reference_dlls.get(record.dll_id)
        dll_entry = dll_entries_by_id.get(record.dll_id)
        family_name = reference_dll.name if reference_dll is not None and reference_dll.name else (
            same_dll_records[0].name if len(same_dll_records) == 1 else f"dll_{record.dll_id:04X}"
        )
        class_output_name = class_packet_name(record.class_id & 0xFFFF, class_name, same_class_records)
        family_output_name = family_packet_name(record.dll_id, reference_dll, same_dll_records)
        boundary_kind = classify_boundary(record, same_class_records, same_dll_records)
        score = score_packet(
            record=record,
            widths=widths,
            same_class_records=same_class_records,
            same_dll_records=same_dll_records,
            reference_object=reference_object,
            reference_dll=reference_dll,
            dll_entry=dll_entry,
        )
        packets.append(
            DefPacket(
                record=record,
                score=score,
                boundary_kind=boundary_kind,
                class_name=class_name,
                family_name=family_name,
                output_name=f"obj_{record.def_id:04X}_{slugify(record.name)}.c",
                class_output_name=class_output_name,
                family_output_name=family_output_name,
                placement_widths=widths,
                same_class_records=same_class_records,
                same_dll_records=same_dll_records,
                reference_object=reference_object,
                reference_dll=reference_dll,
                dll_entry=dll_entry,
            )
        )

    packets.sort(
        key=lambda item: (
            -item.score,
            -item.record.placements,
            len(item.same_class_records),
            len(item.same_dll_records),
            item.record.def_id,
        )
    )
    return packets


def packet_matches(packet: DefPacket, pattern: str) -> bool:
    record = packet.record
    if pattern.startswith("def:"):
        value = pattern[4:]
        return value in (f"{record.def_id:04x}", f"0x{record.def_id:04x}")
    if pattern.startswith("dll:"):
        value = pattern[4:]
        return value in (f"{record.dll_id:04x}", f"0x{record.dll_id:04x}")
    if pattern.startswith("class:"):
        class_id = record.class_id & 0xFFFF
        value = pattern[6:]
        return value in (f"{class_id:04x}", f"0x{class_id:04x}")
    if pattern.startswith("map:"):
        value = pattern[4:]
        return record.map_name is not None and value in record.map_name.lower()
    if pattern.startswith("width:"):
        value = pattern[6:]
        return any(value in (f"{width}w", f"0x{width * 4:X}") for width in packet.placement_widths)
    if pattern.startswith("boundary:"):
        return pattern[9:] in packet.boundary_kind.lower()

    haystacks = [
        f"{record.def_id:04x}",
        f"0x{record.def_id:04x}",
        f"{record.dll_id:04x}",
        f"0x{record.dll_id:04x}",
        f"{record.class_id & 0xFFFF:04x}",
        f"0x{record.class_id & 0xFFFF:04x}",
        record.name.lower(),
        packet.output_name.lower(),
        packet.boundary_kind.lower(),
        packet.class_name.lower(),
        packet.family_name.lower(),
    ]
    if record.map_name is not None:
        haystacks.append(record.map_name.lower())
    haystacks.extend(item.name.lower() for item in packet.same_class_records[:16])
    haystacks.extend(item.name.lower() for item in packet.same_dll_records[:16])
    if packet.reference_object is not None:
        if packet.reference_object.name:
            haystacks.append(packet.reference_object.name.lower())
        if packet.reference_object.description:
            haystacks.append(packet.reference_object.description.lower())
        haystacks.extend(param.name.lower() for param in packet.reference_object.params if param.name)
    if packet.reference_dll is not None:
        if packet.reference_dll.name:
            haystacks.append(packet.reference_dll.name.lower())
        if packet.reference_dll.description:
            haystacks.append(packet.reference_dll.description.lower())
        haystacks.extend(function.name.lower() for function in packet.reference_dll.functions if function.name)
        haystacks.extend(field.name.lower() for field in packet.reference_dll.fields if field.name)
    if packet.dll_entry is not None:
        haystacks.extend(slot.symbol.lower() for slot in packet.dll_entry.slots if slot.symbol is not None)
    return any(pattern in value for value in haystacks)


def filter_packets(
    packets: list[DefPacket],
    patterns: list[str],
    include_unplaced: bool,
    descriptor_only: bool,
    min_placements: int | None,
    max_class_defs: int | None,
    max_dll_defs: int | None,
) -> list[DefPacket]:
    filtered = packets
    if not include_unplaced:
        filtered = [packet for packet in filtered if packet.record.placements > 0]
    if descriptor_only:
        filtered = [
            packet
            for packet in filtered
            if packet.dll_entry is not None and packet.dll_entry.status == "descriptor"
        ]
    if min_placements is not None:
        filtered = [packet for packet in filtered if packet.record.placements >= min_placements]
    if max_class_defs is not None:
        filtered = [packet for packet in filtered if len(packet.same_class_records) <= max_class_defs]
    if max_dll_defs is not None:
        filtered = [packet for packet in filtered if len(packet.same_dll_records) <= max_dll_defs]
    if patterns:
        lowered = [pattern.lower() for pattern in patterns]
        filtered = [packet for packet in filtered if all(packet_matches(packet, pattern) for pattern in lowered)]
    return filtered


