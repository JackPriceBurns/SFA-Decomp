def detailed_markdown(
    packets: list[FamilyPacket],
    widths_by_def: dict[int, Counter[int]],
    romlists_by_def_width: dict[tuple[int, int], set[str]],
) -> str:
    lines: list[str] = []
    lines.append("# Object-family search")
    lines.append("")
    if not packets:
        lines.append("- No matching descriptor-backed families.")
        return "\n".join(lines)

    for packet in packets[:16]:
        lines.append(f"## `0x{packet.dll_id:04X}` `{packet.suggested_name}`")
        lines.append(
            f"- score={packet.score}, placements={packet.total_placements}, defs={packet.object_count}, "
            f"record_sizes={format_sizes_hex(packet.record_sizes)}, placement_widths={format_widths(packet.placement_widths)}"
        )
        lines.append(
            f"- EN descriptor=`0x{packet.dll_entry.target_address:08X}` `{packet.dll_entry.target_symbol or 'unknown'}`, "
            f"slots={packet.dll_entry.function_count}, mask=`{packet.dll_entry.slot_mask}`"
        )
        lines.append(f"- suggested packet path: `src/main/unknown/{packet.output_name}`")
        if packet.reference_class_names:
            lines.append("- reference class names: " + ", ".join(f"`{name}`" for name in packet.reference_class_names))
        if packet.reference_dll is not None:
            ref_bits = []
            if packet.reference_dll.name:
                ref_bits.append(f"name=`{packet.reference_dll.name}`")
            if packet.reference_dll.objparams_size is not None:
                ref_bits.append(f"objparams_size=`0x{packet.reference_dll.objparams_size:X}`")
            if packet.reference_dll.description:
                ref_bits.append(packet.reference_dll.description)
            if ref_bits:
                lines.append("- reference DLL hint: " + ", ".join(ref_bits))

        lines.append("- retail objects:")
        for item in packet.objects[:12]:
            width_counter = widths_by_def.get(item.def_id, Counter())
            width_text = ", ".join(f"{width}w x{count}" for width, count in sorted(width_counter.items())) or "none"
            alias_text = "" if not item.remap_sources else f", aliases={' '.join(f'0x{value:04X}' for value in item.remap_sources)}"
            lines.append(
                f"  - `0x{item.def_id:04X}` `{item.name}`: class=`0x{item.class_id & 0xFFFF:04X}`, "
                f"placements={item.placements}, romlists={item.romlists}, objsize=`0x{item.size:X}`, widths={width_text}{alias_text}"
            )
            if width_counter:
                width_samples = []
                for width in sorted(width_counter):
                    sample_names = sorted(romlists_by_def_width.get((item.def_id, width), set()))[:3]
                    if sample_names:
                        width_samples.append(f"{width}w via {' '.join(sample_names)}")
                if width_samples:
                    lines.append("    samples: " + "; ".join(width_samples))
        if len(packet.objects) > 12:
            lines.append(f"  - ... {len(packet.objects) - 12} more objects omitted")

        lines.append("- EN slot map:")
        reference_functions = function_name_by_index(packet.reference_dll)
        for slot in packet.dll_entry.slots:
            if slot.address == 0:
                continue
            ref = reference_functions.get(slot.index)
            ref_text = ""
            if ref is not None and ref.name is not None:
                suffix = " stub" if ref.stub else ""
                ref_text = f", ref={ref.name}{suffix}"
            stub_text = "" if slot.stub_kind is None else f", stub={slot.stub_kind}"
            lines.append(
                f"  - slot {slot.index:02d}: `0x{slot.address:08X}` `{slot.symbol or 'unknown'}`{stub_text}{ref_text}"
            )

        if packet.reference_dll is not None and packet.reference_dll.fields:
            lines.append("- reference-only objparam fields:")
            for field in packet.reference_dll.fields[:12]:
                field_name = field.name or "unnamed"
                description = "" if field.description is None else f" - {field.description}"
                lines.append(
                    f"  - `0x{field.offset:02X}` `{field_name}` `{field.type_name}`{description}"
                )
            if len(packet.reference_dll.fields) > 12:
                lines.append(f"  - ... {len(packet.reference_dll.fields) - 12} more fields omitted")
        lines.append("")
    return "\n".join(lines).rstrip()


def rows_to_csv(packets: list[FamilyPacket]) -> str:
    fieldnames = [
        "dll_id",
        "dll_id_hex",
        "score",
        "suggested_name",
        "placements",
        "object_defs",
        "record_sizes",
        "placement_widths",
        "slot_count",
        "slot_mask",
        "reference_name",
        "output_name",
        "sample_objects",
    ]
    buffer = io.StringIO()
    writer = csv.DictWriter(buffer, fieldnames=fieldnames)
    writer.writeheader()
    for packet in packets:
        writer.writerow(
            {
                "dll_id": packet.dll_id,
                "dll_id_hex": f"0x{packet.dll_id:04X}",
                "score": packet.score,
                "suggested_name": packet.suggested_name,
                "placements": packet.total_placements,
                "object_defs": packet.object_count,
                "record_sizes": " ".join(f"0x{size:X}" for size in packet.record_sizes),
                "placement_widths": " ".join(f"{width}w" for width in packet.placement_widths),
                "slot_count": packet.dll_entry.function_count,
                "slot_mask": packet.dll_entry.slot_mask,
                "reference_name": "" if packet.reference_dll is None or packet.reference_dll.name is None else packet.reference_dll.name,
                "output_name": packet.output_name,
                "sample_objects": " | ".join(f"0x{item.def_id:04X}:{item.name}" for item in packet.objects[:6]),
            }
        )
    return buffer.getvalue()


def render_packet_stub(
    packet: FamilyPacket,
    output_name: str,
    widths_by_def: dict[int, Counter[int]],
) -> str:
    reference_functions = function_name_by_index(packet.reference_dll)
    lines: list[str] = []
    lines.append("/*")
    lines.append(f" * {GENERATED_MARKER}")
    lines.append(" *")
    lines.append(" * This file is intentionally not wired into the build yet.")
    lines.append(" * It exists as a makeshift source-file boundary for object/DLL recovery work.")
    lines.append(" *")
    lines.append(f" * DLL ID: 0x{packet.dll_id:04X}")
    lines.append(f" * Suggested family name: {packet.suggested_name}")
    lines.append(f" * Output path: src/main/unknown/{output_name}")
    lines.append(f" * Retail placements: {packet.total_placements}")
    lines.append(f" * Retail object defs: {packet.object_count}")
    lines.append(f" * Retail object record sizes: {format_sizes_hex(packet.record_sizes)}")
    lines.append(f" * Retail root placement widths: {format_widths(packet.placement_widths)}")
    lines.append(
        f" * EN descriptor: 0x{packet.dll_entry.target_address:08X} "
        f"{packet.dll_entry.target_symbol or 'unknown'}"
    )
    lines.append(
        f" * EN descriptor shape: slots={packet.dll_entry.function_count} mask={packet.dll_entry.slot_mask}"
    )
    lines.append(" *")
    lines.append(" * Retail object defs:")
    for item in packet.objects:
        widths = widths_by_def.get(item.def_id, Counter())
        width_text = ", ".join(f"{width}w x{count}" for width, count in sorted(widths.items())) or "none"
        alias_text = "" if not item.remap_sources else f", aliases={' '.join(f'0x{value:04X}' for value in item.remap_sources)}"
        lines.append(
            f" * - 0x{item.def_id:04X} {item.name}: class=0x{item.class_id & 0xFFFF:04X}, "
            f"placements={item.placements}, romlists={item.romlists}, objsize=0x{item.size:X}, widths={width_text}{alias_text}"
        )
    lines.append(" *")
    lines.append(" * EN slot map with reference-only names by slot index:")
    for slot in packet.dll_entry.slots:
        if slot.address == 0:
            continue
        ref = reference_functions.get(slot.index)
        ref_text = ""
        if ref is not None and ref.name is not None:
            ref_text = f" ref={ref.name}"
            if ref.stub:
                ref_text += " (stub)"
        stub_text = "" if slot.stub_kind is None else f" stub={slot.stub_kind}"
        lines.append(
            f" * - slot {slot.index:02d}: 0x{slot.address:08X} {slot.symbol or 'unknown'}{stub_text}{ref_text}"
        )
    if packet.reference_dll is not None:
        lines.append(" *")
        lines.append(" * Reference-only hints:")
        if packet.reference_dll.name is not None:
            lines.append(f" * - reference DLL name: {packet.reference_dll.name}")
        if packet.reference_dll.description is not None:
            lines.append(f" * - reference description: {packet.reference_dll.description}")
        if packet.reference_dll.objparams_size is not None:
            lines.append(f" * - reference objparams size: 0x{packet.reference_dll.objparams_size:X}")
        for field in packet.reference_dll.fields[:12]:
            field_name = field.name or "unnamed"
            desc = "" if field.description is None else f" - {field.description}"
            lines.append(
                f" * - field 0x{field.offset:02X}: {field_name} ({field.type_name}){desc}"
            )
        if len(packet.reference_dll.fields) > 12:
            lines.append(f" * - ... {len(packet.reference_dll.fields) - 12} more reference fields omitted")
    lines.append(" */")
    lines.append("")
    lines.append("#if 0")
    lines.append("/*")
    lines.append(" * Reference-only slot names. Verify against EN before promoting any of these into live code.")
    lines.append(" */")
    enum_name = identifier_from_name(f"dll_{packet.dll_id:04X}_{packet.suggested_name}_slot")
    lines.append(f"enum {enum_name} {{")
    for slot in packet.dll_entry.slots:
        if slot.address == 0:
            continue
        ref = reference_functions.get(slot.index)
        slot_name = f"slot_{slot.index:02d}" if ref is None or ref.name is None else ref.name
        lines.append(
            f"    {identifier_from_name(f'{packet.suggested_name}_{slot_name}').upper()} = {slot.index},"
        )
    lines.append("};")
    lines.append("#endif")
    lines.append("")
    return "\n".join(lines)


def materialize_packets(
    packets: list[FamilyPacket],
    output_root: Path,
    widths_by_def: dict[int, Counter[int]],
) -> tuple[list[str], list[str], list[str]]:
    written: list[str] = []
    unchanged: list[str] = []
    skipped: list[str] = []
    for packet in packets:
        dst_path = output_root / packet.output_name
        if dst_path.is_file() and not is_managed_generated_stub(dst_path):
            skipped.append(packet.output_name)
            continue
        text = render_packet_stub(packet, packet.output_name, widths_by_def)
        changed = write_text_if_changed(dst_path, text)
        if changed:
            written.append(packet.output_name)
        else:
            unchanged.append(packet.output_name)
    return written, unchanged, skipped


