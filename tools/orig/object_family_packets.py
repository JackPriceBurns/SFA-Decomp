GENERATED_MARKER = "Auto-generated by tools/orig/object_family_packets.py."
IDENTIFIER_RE = re.compile(r"[^A-Za-z0-9_]+")


@dataclass(frozen=True)
class ReferenceFunctionHint:
    index: int
    name: str | None
    valid: bool
    stub: bool


@dataclass(frozen=True)
class ReferenceFieldHint:
    offset: int
    type_name: str
    name: str | None
    description: str | None


@dataclass(frozen=True)
class ReferenceDllHint:
    dll_id: int
    name: str | None
    description: str | None
    objparams_size: int | None
    functions: tuple[ReferenceFunctionHint, ...]
    fields: tuple[ReferenceFieldHint, ...]


@dataclass(frozen=True)
class FamilyPacket:
    dll_id: int
    score: int
    suggested_name: str
    output_name: str
    total_placements: int
    object_count: int
    romlist_count: int
    record_sizes: tuple[int, ...]
    placement_widths: tuple[int, ...]
    objects: tuple[ObjectRecord, ...]
    dll_entry: DllEntry
    reference_dll: ReferenceDllHint | None
    reference_class_names: tuple[str, ...]


def clean_text(value: str | None) -> str | None:
    if value is None:
        return None
    text = " ".join(value.split())
    return text or None


def parse_int(value: str | None) -> int | None:
    if value is None or value == "":
        return None
    return int(value, 0)


def slugify(value: str) -> str:
    slug = IDENTIFIER_RE.sub("_", value).strip("_").lower()
    return slug or "family"


def identifier_from_name(value: str) -> str:
    identifier = IDENTIFIER_RE.sub("_", value).strip("_")
    if not identifier:
        identifier = "unnamed"
    if identifier[0].isdigit():
        identifier = "_" + identifier
    return identifier


def write_text_if_changed(path: Path, text: str) -> bool:
    if path.is_file() and path.read_text(encoding="utf-8") == text:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8", newline="\n")
    return True


def is_managed_generated_stub(path: Path) -> bool:
    if not path.is_file():
        return False
    return GENERATED_MARKER in path.read_text(encoding="utf-8", errors="replace")[:512]


def load_reference_dll_hints(path: Path) -> dict[int, ReferenceDllHint]:
    if not path.is_file():
        return {}

    root = ET.parse(path).getroot()
    hints: dict[int, ReferenceDllHint] = {}
    for element in root.findall("dll"):
        dll_id = parse_int(element.attrib.get("id"))
        if dll_id is None:
            continue

        functions: list[ReferenceFunctionHint] = []
        functions_parent = element.find("functions")
        if functions_parent is not None:
            for function_element in functions_parent.findall("function"):
                index = parse_int(function_element.attrib.get("idx"))
                if index is None:
                    continue
                name = clean_text(function_element.attrib.get("name"))
                valid = function_element.attrib.get("valid", "1") != "0"
                stub = function_element.attrib.get("stub", "0") == "1"
                functions.append(
                    ReferenceFunctionHint(
                        index=index,
                        name=name,
                        valid=valid,
                        stub=stub,
                    )
                )

        fields: list[ReferenceFieldHint] = []
        objparams = element.find("objparams")
        objparams_size = parse_int(None if objparams is None else objparams.attrib.get("size"))
        if objparams is not None:
            for field in objparams.findall("field"):
                offset = parse_int(field.attrib.get("offset"))
                type_name = clean_text(field.attrib.get("type")) or "unknown"
                if offset is None:
                    continue
                fields.append(
                    ReferenceFieldHint(
                        offset=offset,
                        type_name=type_name,
                        name=clean_text(field.attrib.get("name")),
                        description=clean_text(field.findtext("description") or field.findtext("note")),
                    )
                )

        hints[dll_id] = ReferenceDllHint(
            dll_id=dll_id,
            name=clean_text(element.attrib.get("name")),
            description=clean_text(element.findtext("description")),
            objparams_size=objparams_size,
            functions=tuple(sorted(functions, key=lambda item: item.index)),
            fields=tuple(sorted(fields, key=lambda item: item.offset)),
        )
    return hints


def load_reference_class_names(path: Path) -> dict[int, str]:
    if not path.is_file():
        return {}

    root = ET.parse(path).getroot()
    names: dict[int, str] = {}
    for element in root.findall("cat"):
        class_id = parse_int(element.attrib.get("id"))
        name = clean_text(element.attrib.get("name"))
        if class_id is None or name is None:
            continue
        names[class_id] = name
    return names


def load_root_param_widths(
    files_root: Path,
    objindex: list[int],
) -> tuple[dict[int, Counter[int]], dict[tuple[int, int], set[str]]]:
    widths_by_def: dict[int, Counter[int]] = defaultdict(Counter)
    romlists_by_def_width: dict[tuple[int, int], set[str]] = defaultdict(set)
    for romlist_path in sorted(files_root.glob("*.romlist.zlb")):
        decompressed = decompress_zlb(romlist_path)
        offset = 0
        while offset < len(decompressed):
            object_id, size_words, _flags = (
                int.from_bytes(decompressed[offset : offset + 2], "big", signed=True),
                decompressed[offset + 2],
                decompressed[offset + 3],
            )
            record_size = size_words * 4
            if record_size <= 0 or offset + record_size > len(decompressed):
                raise ValueError(
                    f"Invalid record in {romlist_path.name}: offset=0x{offset:X} size_words={size_words}"
                )
            canonical_id = resolve_object_id(objindex, object_id)
            widths_by_def[canonical_id][size_words] += 1
            romlists_by_def_width[(canonical_id, size_words)].add(romlist_path.name)
            offset += record_size
    return widths_by_def, romlists_by_def_width


def build_family_packets(
    records: list[ObjectRecord],
    dll_entries: list[DllEntry],
    widths_by_def: dict[int, Counter[int]],
    reference_dlls: dict[int, ReferenceDllHint],
    reference_classes: dict[int, str],
) -> list[FamilyPacket]:
    records_by_dll: dict[int, list[ObjectRecord]] = defaultdict(list)
    for record in records:
        if record.dll_id != 0xFFFF:
            records_by_dll[record.dll_id].append(record)

    entry_by_dll = {entry.dll_id: entry for entry in dll_entries}
    packets: list[FamilyPacket] = []
    for dll_id, items in records_by_dll.items():
        entry = entry_by_dll.get(dll_id)
        if entry is None or entry.status != "descriptor":
            continue

        items.sort(key=lambda item: (-item.placements, item.def_id))
        reference_dll = reference_dlls.get(dll_id)
        class_names = sorted(
            {
                reference_classes[class_id]
                for class_id in {item.class_id & 0xFFFF for item in items}
                if class_id in reference_classes
            }
        )
        record_sizes = tuple(sorted({item.size for item in items}))
        width_values = tuple(sorted({width for item in items for width in widths_by_def.get(item.def_id, Counter())}))
        total_placements = sum(item.placements for item in items)
        romlist_count = len(
            {
                item.def_id
                for item in items
                if item.romlists
            }
        )

        name_source = (
            reference_dll.name
            or (items[0].name if len(items) == 1 else f"dll_{dll_id:04X}")
        )
        output_name = f"dll_{dll_id:04X}_{slugify(name_source)}.c"

        score = total_placements
        if reference_dll is not None:
            score += 400
            if reference_dll.functions:
                score += 80
            if reference_dll.fields:
                score += 80
        if len(items) == 1:
            score += 200
        elif len(items) <= 10:
            score += 100
        if len(width_values) == 1:
            score += 120
        elif 1 < len(width_values) <= 4:
            score += 60
        if len(record_sizes) == 1:
            score += 40

        packets.append(
            FamilyPacket(
                dll_id=dll_id,
                score=score,
                suggested_name=name_source,
                output_name=output_name,
                total_placements=total_placements,
                object_count=len(items),
                romlist_count=romlist_count,
                record_sizes=record_sizes,
                placement_widths=width_values,
                objects=tuple(items),
                dll_entry=entry,
                reference_dll=reference_dll,
                reference_class_names=tuple(class_names),
            )
        )

    packets.sort(
        key=lambda item: (
            -item.score,
            -item.total_placements,
            item.object_count,
            item.dll_id,
        )
    )
    return packets


def format_sizes_hex(values: tuple[int, ...]) -> str:
    return ", ".join(f"0x{value:X}" for value in values) if values else "-"


def format_widths(values: tuple[int, ...]) -> str:
    return ", ".join(f"{value}w" for value in values) if values else "-"


def function_name_by_index(reference_dll: ReferenceDllHint | None) -> dict[int, ReferenceFunctionHint]:
    if reference_dll is None:
        return {}
    return {item.index: item for item in reference_dll.functions}


def packet_matches(packet: FamilyPacket, pattern: str) -> bool:
    if pattern.startswith("dll:"):
        value = pattern[4:]
        return value in (f"{packet.dll_id:04x}", f"0x{packet.dll_id:04x}")
    if pattern.startswith("def:"):
        value = pattern[4:]
        return any(value in (f"{item.def_id:04x}", f"0x{item.def_id:04x}") for item in packet.objects)
    if pattern.startswith("class:"):
        value = pattern[6:]
        return any(
            value in (f"{item.class_id & 0xFFFF:04x}", f"0x{item.class_id & 0xFFFF:04x}")
            for item in packet.objects
        )

    haystacks = [
        f"{packet.dll_id:04x}",
        f"0x{packet.dll_id:04x}",
        packet.suggested_name.lower(),
        packet.output_name.lower(),
        packet.dll_entry.slot_mask.lower(),
    ]
    if packet.reference_dll is not None:
        if packet.reference_dll.name:
            haystacks.append(packet.reference_dll.name.lower())
        if packet.reference_dll.description:
            haystacks.append(packet.reference_dll.description.lower())
        haystacks.extend(
            item.name.lower()
            for item in packet.reference_dll.functions
            if item.name
        )
        haystacks.extend(
            item.name.lower()
            for item in packet.reference_dll.fields
            if item.name
        )
    haystacks.extend(item.name.lower() for item in packet.objects)
    haystacks.extend(packet.reference_class_names)
    haystacks.extend(
        slot.symbol.lower()
        for slot in packet.dll_entry.slots
        if slot.symbol is not None
    )
    return any(pattern in value for value in haystacks)


def filter_packets(packets: list[FamilyPacket], patterns: list[str]) -> list[FamilyPacket]:
    if not patterns:
        return packets
    lowered = [pattern.lower() for pattern in patterns]
    return [packet for packet in packets if all(packet_matches(packet, pattern) for pattern in lowered)]


