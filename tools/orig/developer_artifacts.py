from __future__ import annotations

import argparse
import csv
import hashlib
import io
import re
import sys
from collections import Counter, defaultdict
from dataclasses import dataclass
from pathlib import Path


REGIONS = ("GSAE01", "GSAP01", "GSAJ01")
PRINTABLE_RE = re.compile(rb"[ -~]{4,}")
SOURCE_NAME_RE = re.compile(r"([A-Za-z0-9_./<>-]+\.(?:c|cpp|h|hpp))")
DEFINE_RE = re.compile(r"^#define\s+(\S+)\s+(\d+)$", re.MULTILINE)
INCLUDE_RE = re.compile(r'^#include\s+"([^"]+)"', re.MULTILINE)
ARRAY_RE = re.compile(r"^([A-Za-z_][A-Za-z0-9_ \t\*<>]*)\s+([A-Za-z_][A-Za-z0-9_]*)\[\]\s*=\s*\{", re.MULTILINE)
FILE_HEADER_RE = re.compile(r"^\s*File:\s+(.+?)\s*$", re.MULTILINE)
GENERATED_BY_RE = re.compile(r"Generated by ([^\r\n]+)")
TEXT_SYMBOL_RE = re.compile(r"^[A-Za-z]+_(\d+)_\d+$")


@dataclass(frozen=True)
class RegionDigest:
    region: str
    exists: bool
    size: int | None
    sha1: str | None


@dataclass(frozen=True)
class GeneratedSourceArtifact:
    relative_path: str
    region_digests: tuple[RegionDigest, ...]
    internal_path: str
    includes: tuple[str, ...]
    array_names: tuple[str, ...]
    text_ids: tuple[int, ...]
    character_entry_count: int
    text_entry_count: int
    normalized_sha1: str


@dataclass(frozen=True)
class AudioHeaderArtifact:
    relative_path: str
    region_digests: tuple[RegionDigest, ...]
    generated_by: str | None
    define_counts: tuple[tuple[str, int], ...]
    first_names: tuple[str, ...]
    last_names: tuple[str, ...]


@dataclass(frozen=True)
class ModuleArtifact:
    relative_path: str
    region_digests: tuple[RegionDigest, ...]
    kind: str
    detail: str


@dataclass(frozen=True)
class BinaryLeakArtifact:
    relative_path: str
    region_digests: tuple[RegionDigest, ...]
    source_names: tuple[str, ...]


Artifact = GeneratedSourceArtifact | AudioHeaderArtifact | ModuleArtifact | BinaryLeakArtifact


def sha1_bytes(data: bytes) -> str:
    return hashlib.sha1(data).hexdigest()


def digest_for_path(path: Path) -> RegionDigest:
    if not path.is_file():
        return RegionDigest(region=path.parts[-3], exists=False, size=None, sha1=None)
    data = path.read_bytes()
    return RegionDigest(
        region=path.parts[-3],
        exists=True,
        size=len(data),
        sha1=sha1_bytes(data),
    )


def region_digests(orig_root: Path, relative_path: str) -> tuple[RegionDigest, ...]:
    digests: list[RegionDigest] = []
    for region in REGIONS:
        path = orig_root / region / relative_path
        if path.is_file():
            data = path.read_bytes()
            digests.append(
                RegionDigest(
                    region=region,
                    exists=True,
                    size=len(data),
                    sha1=sha1_bytes(data),
                )
            )
        else:
            digests.append(
                RegionDigest(
                    region=region,
                    exists=False,
                    size=None,
                    sha1=None,
                )
            )
    return tuple(digests)


def stable_across_regions(digests: tuple[RegionDigest, ...]) -> bool:
    values = {digest.sha1 for digest in digests if digest.sha1 is not None}
    return len(values) == 1 and bool(values)


def normalize_generated_source(text: str) -> str:
    return FILE_HEADER_RE.sub("  File:    <normalized>", text)


def count_initializer_entries(text: str, symbol_name: str) -> int:
    match = re.search(rf"{re.escape(symbol_name)}\[\]\s*=\s*\{{(.*?)\n\}};", text, re.DOTALL)
    if match is None:
        return 0
    return match.group(1).count("{")


def analyze_generated_source(orig_root: Path, relative_path: str) -> GeneratedSourceArtifact:
    path = orig_root / "GSAE01" / relative_path
    text = path.read_text(encoding="utf-8", errors="replace")
    internal_path_match = FILE_HEADER_RE.search(text)
    internal_path = internal_path_match.group(1) if internal_path_match else ""
    includes = tuple(sorted(set(INCLUDE_RE.findall(text))))
    array_names = tuple(match.group(2) for match in ARRAY_RE.finditer(text))
    text_ids = sorted(
        {
            int(match.group(1))
            for array_name in array_names
            if (match := TEXT_SYMBOL_RE.match(array_name)) is not None
        }
    )
    character_entry_count = count_initializer_entries(text, next((name for name in array_names if name.endswith("_characters")), ""))
    text_entry_count = count_initializer_entries(text, next((name for name in array_names if name.endswith("_texts")), ""))
    normalized_sha1 = sha1_bytes(normalize_generated_source(text).encode("utf-8"))
    return GeneratedSourceArtifact(
        relative_path=relative_path,
        region_digests=region_digests(orig_root, relative_path),
        internal_path=internal_path,
        includes=includes,
        array_names=array_names,
        text_ids=tuple(text_ids),
        character_entry_count=character_entry_count,
        text_entry_count=text_entry_count,
        normalized_sha1=normalized_sha1,
    )


def define_prefix(name: str) -> str:
    for prefix in ("GRP", "SNG", "SFX"):
        if name.startswith(prefix):
            return prefix
    return name.split("_", 1)[0]


def analyze_audio_header(orig_root: Path, relative_path: str) -> AudioHeaderArtifact:
    path = orig_root / "GSAE01" / relative_path
    text = path.read_text(encoding="utf-8", errors="replace")
    generated_by_match = GENERATED_BY_RE.search(text)
    defines = DEFINE_RE.findall(text)
    counts = Counter(define_prefix(name) for name, _value in defines)
    names = [name for name, _value in defines]
    return AudioHeaderArtifact(
        relative_path=relative_path,
        region_digests=region_digests(orig_root, relative_path),
        generated_by=None if generated_by_match is None else generated_by_match.group(1),
        define_counts=tuple(sorted(counts.items())),
        first_names=tuple(names[:8]),
        last_names=tuple(names[-8:]),
    )


def analyze_module_path(orig_root: Path, relative_path: str) -> ModuleArtifact:
    path = orig_root / "GSAE01" / relative_path
    data = path.read_bytes()
    if path.suffix.lower() == ".str":
        detail = data.split(b"\0", 1)[0].decode("ascii", "replace")
        kind = "path-string"
    else:
        detail = f"{len(data)} bytes"
        kind = "binary-stub"
    return ModuleArtifact(
        relative_path=relative_path,
        region_digests=region_digests(orig_root, relative_path),
        kind=kind,
        detail=detail,
    )


def scan_source_names(data: bytes) -> tuple[str, ...]:
    names: list[str] = []
    seen: set[str] = set()
    for raw in PRINTABLE_RE.findall(data):
        text = raw.decode("ascii", "ignore")
        for match in SOURCE_NAME_RE.finditer(text):
            name = match.group(1)
            if name in seen:
                continue
            seen.add(name)
            names.append(name)
    return tuple(names)


def analyze_binary_leaks(orig_root: Path, relative_path: str) -> BinaryLeakArtifact:
    path = orig_root / "GSAE01" / relative_path
    data = path.read_bytes()
    return BinaryLeakArtifact(
        relative_path=relative_path,
        region_digests=region_digests(orig_root, relative_path),
        source_names=scan_source_names(data),
    )


def collect_artifacts(orig_root: Path) -> list[Artifact]:
    artifacts: list[Artifact] = []
    for relative_path in (
        "files/Boot/English.c.new",
        "files/Boot/French.c.new",
        "files/Boot/German.c.new",
        "files/Boot/Italian.c.new",
        "files/Boot/Spanish.c.new",
        "files/gametext/Boot/English.c.new",
        "files/gametext/Boot/French.c.new",
        "files/gametext/Boot/German.c.new",
        "files/gametext/Boot/Italian.c.new",
        "files/gametext/Boot/Japanese.c.new",
        "files/gametext/Boot/Spanish.c.new",
    ):
        if (orig_root / "GSAE01" / relative_path).is_file():
            artifacts.append(analyze_generated_source(orig_root, relative_path))

    for relative_path in (
        "files/audio/starfox.h.bak",
        "files/modules/dino.str",
        "files/modules/testmod.rel",
        "sys/apploader.img",
    ):
        path = orig_root / "GSAE01" / relative_path
        if not path.is_file():
            continue
        if relative_path.endswith(".bak"):
            artifacts.append(analyze_audio_header(orig_root, relative_path))
        elif relative_path.startswith("files/modules/"):
            artifacts.append(analyze_module_path(orig_root, relative_path))
        else:
            artifacts.append(analyze_binary_leaks(orig_root, relative_path))
    return artifacts


def artifact_kind(artifact: Artifact) -> str:
    if isinstance(artifact, GeneratedSourceArtifact):
        return "generated-source"
    if isinstance(artifact, AudioHeaderArtifact):
        return "audio-header"
    if isinstance(artifact, ModuleArtifact):
        return f"module-{artifact.kind}"
    return "binary-source-leak"


def generated_source_pairs(artifacts: list[Artifact]) -> dict[str, list[GeneratedSourceArtifact]]:
    pairs: dict[str, list[GeneratedSourceArtifact]] = defaultdict(list)
    for artifact in artifacts:
        if not isinstance(artifact, GeneratedSourceArtifact):
            continue
        key = "/".join(artifact.relative_path.split("/")[-2:])
        pairs[key].append(artifact)
    return pairs


def find_patterns(artifact: Artifact) -> list[str]:
    fields = [artifact.relative_path, artifact_kind(artifact)]
    if isinstance(artifact, GeneratedSourceArtifact):
        fields.append(artifact.internal_path)
        fields.extend(artifact.includes)
        fields.extend(artifact.array_names)
        fields.extend(str(text_id) for text_id in artifact.text_ids)
    elif isinstance(artifact, AudioHeaderArtifact):
        if artifact.generated_by is not None:
            fields.append(artifact.generated_by)
        fields.extend(name for name, _count in artifact.define_counts)
        fields.extend(artifact.first_names)
        fields.extend(artifact.last_names)
    elif isinstance(artifact, ModuleArtifact):
        fields.extend((artifact.kind, artifact.detail))
    else:
        fields.extend(artifact.source_names)
    return [field.lower() for field in fields]


def search_artifacts(artifacts: list[Artifact], patterns: list[str]) -> list[Artifact]:
    lowered = [pattern.lower() for pattern in patterns]
    matches: list[Artifact] = []
    for artifact in artifacts:
        fields = find_patterns(artifact)
        if all(any(pattern in field for field in fields) for pattern in lowered):
            matches.append(artifact)
    return matches


def summary_markdown(artifacts: list[Artifact]) -> str:
    generated = [artifact for artifact in artifacts if isinstance(artifact, GeneratedSourceArtifact)]
    audio = [artifact for artifact in artifacts if isinstance(artifact, AudioHeaderArtifact)]
    modules = [artifact for artifact in artifacts if isinstance(artifact, ModuleArtifact)]
    binary_leaks = [artifact for artifact in artifacts if isinstance(artifact, BinaryLeakArtifact)]
    pair_map = generated_source_pairs(artifacts)

    lines: list[str] = []
    lines.append("# `orig/` developer-artifact catalog")
    lines.append("")
    lines.append("## Summary")
    lines.append(f"- Generated source fragments: `{len(generated)}`")
    lines.append(f"- Generated headers: `{len(audio)}`")
    lines.append(f"- Module leftovers: `{len(modules)}`")
    lines.append(f"- Binary source-name leaks: `{len(binary_leaks)}`")
    lines.append("")

    lines.append("## Generated source fragments")
    lines.append("- Files under `files/Boot` and `files/gametext/Boot` are real generated C sources, not opaque blobs.")
    pair_statuses: list[tuple[str, bool]] = []
    for key in sorted(pair_map):
        pair = sorted(pair_map[key], key=lambda item: item.relative_path)
        if len(pair) != 2:
            continue
        left, right = pair
        same_payload = left.normalized_sha1 == right.normalized_sha1
        pair_statuses.append((key, same_payload))
        lines.append(
            f"- `{key}`: normalized duplicate across both locations=`{same_payload}`, "
            f"internal paths=`{left.internal_path}` / `{right.internal_path}`"
        )
    exact_pairs = [key for key, same in pair_statuses if same]
    revised_pairs = [key for key, same in pair_statuses if not same]
    if exact_pairs:
        lines.append("- Exact path-normalized duplicate pair(s): " + ", ".join(f"`{key}`" for key in exact_pairs))
    if revised_pairs:
        lines.append("- Revised non-duplicate pair(s): " + ", ".join(f"`{key}`" for key in revised_pairs))
    jp_only = next(
        (
            artifact
            for artifact in generated
            if artifact.relative_path == "files/gametext/Boot/Japanese.c.new"
        ),
        None,
    )
    if jp_only is not None:
        lines.append(
            "- `files/gametext/Boot/Japanese.c.new` exists even though `files/Boot/Japanese.c.new` does not, "
            "which is a useful hint about how the boot-text generation pipeline was staged."
        )
    for artifact in generated[:4]:
        lines.append(
            f"- `{artifact.relative_path}`: includes={', '.join(f'`{item}`' for item in artifact.includes)}, "
            f"char_entries={artifact.character_entry_count}, text_entries={artifact.text_entry_count}, "
            f"text_ids={', '.join(f'`{text_id}`' for text_id in artifact.text_ids)}"
        )
    lines.append("")

    if audio:
        header = audio[0]
        counts = ", ".join(f"`{name}`={count}" for name, count in header.define_counts)
        lines.append("## Generated audio header")
        lines.append(
            f"- `{header.relative_path}` is byte-identical across EN/PAL/JP=`{stable_across_regions(header.region_digests)}`"
        )
        if header.generated_by is not None:
            lines.append(f"- Preserved tool banner: `{header.generated_by}`")
        lines.append(f"- Define families: {counts}")
        lines.append("- First symbols: " + ", ".join(f"`{name}`" for name in header.first_names))
        lines.append("- Last symbols: " + ", ".join(f"`{name}`" for name in header.last_names))
        lines.append("")

    lines.append("## Module leftovers")
    for artifact in modules:
        lines.append(
            f"- `{artifact.relative_path}`: kind=`{artifact.kind}`, detail=`{artifact.detail}`, "
            f"stable_across_regions=`{stable_across_regions(artifact.region_digests)}`"
        )
    lines.append("")

    lines.append("## Binary source-name leaks")
    for artifact in binary_leaks:
        preview = ", ".join(f"`{name}`" for name in artifact.source_names[:8])
        lines.append(
            f"- `{artifact.relative_path}`: stable_across_regions=`{stable_across_regions(artifact.region_digests)}`, "
            f"names={preview}"
        )
    lines.append("")

    lines.append("## Usage")
    lines.append("- Summary: `python tools/orig/developer_artifacts.py`")
    lines.append("- CSV dump: `python tools/orig/developer_artifacts.py --format csv`")
    lines.append("- Search by path, include, array name, text ID, define family, or leaked source name:")
    lines.append("  - `python tools/orig/developer_artifacts.py --search Japanese GameTextData.h`")
    lines.append("  - `python tools/orig/developer_artifacts.py --search SFXdrak`")
    lines.append("  - `python tools/orig/developer_artifacts.py --search apploader dvd.c`")
    return "\n".join(lines)


def artifact_rows(artifacts: list[Artifact]) -> list[dict[str, str]]:
    rows: list[dict[str, str]] = []
    for artifact in artifacts:
        row = {
            "kind": artifact_kind(artifact),
            "path": artifact.relative_path,
            "stable_across_regions": str(stable_across_regions(artifact.region_digests)).lower(),
            "region_presence": ",".join(digest.region for digest in artifact.region_digests if digest.exists),
            "detail": "",
            "extra": "",
        }
        if isinstance(artifact, GeneratedSourceArtifact):
            row["detail"] = artifact.internal_path
            row["extra"] = (
                f"includes={','.join(artifact.includes)};"
                f"char_entries={artifact.character_entry_count};"
                f"text_entries={artifact.text_entry_count};"
                f"text_ids={','.join(str(text_id) for text_id in artifact.text_ids)}"
            )
        elif isinstance(artifact, AudioHeaderArtifact):
            row["detail"] = artifact.generated_by or ""
            row["extra"] = ",".join(f"{name}:{count}" for name, count in artifact.define_counts)
        elif isinstance(artifact, ModuleArtifact):
            row["detail"] = artifact.detail
            row["extra"] = artifact.kind
        else:
            row["detail"] = ",".join(artifact.source_names[:8])
            row["extra"] = f"count={len(artifact.source_names)}"
        rows.append(row)
    return rows


def rows_to_csv(artifacts: list[Artifact]) -> str:
    fieldnames = ["kind", "path", "stable_across_regions", "region_presence", "detail", "extra"]
    buffer = io.StringIO()
    writer = csv.DictWriter(buffer, fieldnames=fieldnames)
    writer.writeheader()
    for row in artifact_rows(artifacts):
        writer.writerow(row)
    return buffer.getvalue()


def search_markdown(artifacts: list[Artifact], patterns: list[str]) -> str:
    matches = search_artifacts(artifacts, patterns)
    lines = ["# Developer artifact search", ""]
    if not matches:
        lines.append("- No matching artifacts.")
        return "\n".join(lines)

    for artifact in matches:
        lines.append(f"- `{artifact.relative_path}` (`{artifact_kind(artifact)}`)")
        if isinstance(artifact, GeneratedSourceArtifact):
            lines.append(
                f"  internal=`{artifact.internal_path}`, includes={', '.join(f'`{item}`' for item in artifact.includes)}, "
                f"text_ids={', '.join(f'`{text_id}`' for text_id in artifact.text_ids)}"
            )
        elif isinstance(artifact, AudioHeaderArtifact):
            counts = ", ".join(f"`{name}`={count}" for name, count in artifact.define_counts)
            lines.append(f"  generated_by=`{artifact.generated_by}`, counts={counts}")
        elif isinstance(artifact, ModuleArtifact):
            lines.append(f"  kind=`{artifact.kind}`, detail=`{artifact.detail}`")
        else:
            lines.append("  source names: " + ", ".join(f"`{name}`" for name in artifact.source_names[:12]))
    return "\n".join(lines)


def build_argument_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Catalog developer-oriented leftovers bundled in orig/.")
    parser.add_argument(
        "--orig-root",
        type=Path,
        default=Path("orig"),
        help="Path to the repo orig/ root.",
    )
    parser.add_argument(
        "--format",
        choices=("markdown", "csv"),
        default="markdown",
        help="Output format.",
    )
    parser.add_argument(
        "--search",
        nargs="+",
        help="Substring search across artifact fields.",
    )
    return parser


def main() -> None:
    parser = build_argument_parser()
    args = parser.parse_args()
    artifacts = collect_artifacts(args.orig_root.resolve())
    try:
        if args.format == "csv":
            sys.stdout.write(rows_to_csv(artifacts))
        elif args.search:
            sys.stdout.write(search_markdown(artifacts, args.search))
            sys.stdout.write("\n")
        else:
            sys.stdout.write(summary_markdown(artifacts))
            sys.stdout.write("\n")
    except BrokenPipeError:
        pass


if __name__ == "__main__":
    main()
